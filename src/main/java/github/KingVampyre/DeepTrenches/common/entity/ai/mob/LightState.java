package github.KingVampyre.DeepTrenches.common.entity.ai.mob;

public class LightState {

    private final String name;

    public LightState(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "LightState{" + "name='" + this.name + '\'' + '}';
    }

}
